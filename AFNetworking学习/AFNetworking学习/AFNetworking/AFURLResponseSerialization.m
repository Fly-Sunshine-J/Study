NSString * const AFURLResponseSerializationErrorDomain = @"com.alamofire.error.serialization.response";
NSString * const AFNetworkingOperationFailingURLResponseErrorKey = @"com.alamofire.serialization.response.error.response";
NSString * const AFNetworkingOperationFailingURLResponseDataErrorKey = @"com.alamofire.serialization.response.error.data";


/**
 根据error和潜在的underlyingError选择一个返回

 @param error error
 @param underlyingError underlyingError
 @return error
 */
static NSError * AFErrorWithUnderlyingError(NSError *error, NSError *underlyingError) {
//    如果error不存在就返回underlyingError
    if (!error) {
        return underlyingError;
    }
//    如果underlyingError不存在或者error的userinfo中的underlyingError存在，返回error
    if (!underlyingError || error.userInfo[NSUnderlyingErrorKey]) {
        return error;
    }
//    获取error和underlyingError将其组合到一起
    NSMutableDictionary *mutableUserInfo = [error.userInfo mutableCopy];
    mutableUserInfo[NSUnderlyingErrorKey] = underlyingError;
//    根据error的信息和新的userinfo生成新的NSError对象返回
    return [[NSError alloc] initWithDomain:error.domain code:error.code userInfo:mutableUserInfo];
}

//判断error或者underlyingError的code和domian是否相同
static BOOL AFErrorOrUnderlyingErrorHasCodeInDomain(NSError *error, NSInteger code, NSString *domain) {
//    如果error的domain和参数domain相等切error的code和code参数相等返回YES
    if ([error.domain isEqualToString:domain] && error.code == code) {
        return YES;
    } else if (error.userInfo[NSUnderlyingErrorKey]) {
//        如果error存在underlyingError递归该方法，返回
        return AFErrorOrUnderlyingErrorHasCodeInDomain(error.userInfo[NSUnderlyingErrorKey], code, domain);
    }

    return NO;
}


/**
 移除value为空的

 @param JSONObject 字典或者数组
 @param readingOptions 读取选择
 @return 字典或者数组
 */
static id AFJSONObjectByRemovingKeysWithNullValues(id JSONObject, NSJSONReadingOptions readingOptions) {
    if ([JSONObject isKindOfClass:[NSArray class]]) {
//        如果是数组
        NSMutableArray *mutableArray = [NSMutableArray arrayWithCapacity:[(NSArray *)JSONObject count]];
        for (id value in (NSArray *)JSONObject) {
//            递归调用
            [mutableArray addObject:AFJSONObjectByRemovingKeysWithNullValues(value, readingOptions)];
        }
//        如果readingOptions是NSJSONReadingMutableContainers，返回可变的数组，否则返回不可变的数组
        return (readingOptions & NSJSONReadingMutableContainers) ? mutableArray : [NSArray arrayWithArray:mutableArray];
    } else if ([JSONObject isKindOfClass:[NSDictionary class]]) {
//        如果是字典
        NSMutableDictionary *mutableDictionary = [NSMutableDictionary dictionaryWithDictionary:JSONObject];
        for (id <NSCopying> key in [(NSDictionary *)JSONObject allKeys]) {
            id value = (NSDictionary *)JSONObject[key];
            if (!value || [value isEqual:[NSNull null]]) {
//                如果value没值或者是NSNull，从字典中移除
                [mutableDictionary removeObjectForKey:key];
            } else if ([value isKindOfClass:[NSArray class]] || [value isKindOfClass:[NSDictionary class]]) {
//                如果value是数组或者字典，递归调用，去除空值和NSNull
                mutableDictionary[key] = AFJSONObjectByRemovingKeysWithNullValues(value, readingOptions);
            }
        }
//        如果readingOptions是NSJSONReadingMutableContainers，返回可变的字典，否则返回不可变的字典
        return (readingOptions & NSJSONReadingMutableContainers) ? mutableDictionary : [NSDictionary dictionaryWithDictionary:mutableDictionary];
    }
//    单个的值
    return JSONObject;
}

